package abook.ajava.utilToy.date;

import java.util.Date;

public class DateDemo05 {
	public static void demo() {
		// Deprecated constructors

		System.out.println("new Date(104, 6, 10) = " + (new Date(104, 6, 10)));
		System.out.println("new Date(104, 7, 10, 22, 10) = "
				+ (new Date(104, 6, 10, 22, 10)));
		System.out.println("new Date(104, 7, 10, 22, 10, 30) = "
				+ (new Date(104, 6, 10, 22, 10, 30)));
	}
}
